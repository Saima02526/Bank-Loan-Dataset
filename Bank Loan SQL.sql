CREATE DATABASE LOAN;
USE LOAN;
CREATE TABLE IF NOT EXISTS FINANCIAL_LOAN (
ID INT,
ADDRESS_STATE VARCHAR(50),
APPLICATION_TYPE VARCHAR(100),
EMP_LENGTH VARCHAR(100),
EMP_TITLE VARCHAR(500),
GRADE VARCHAR(10),
HOME_OWNERSHIP VARCHAR(100),
ISSUE_DATE DATE,
LAST_CREDIT_PULL_DATE DATE,
LAST_PAYMENT_DATE DATE,
LOAN_STATUS VARCHAR(100),
NEXT_PAYMENT_DATE DATE,
MEMBER_ID INT,
PURPOSE VARCHAR(100),
SUB_GRADE VARCHAR(20),
TERM VARCHAR(400),
VERFICATION_STATUS VARCHAR(300),
ANNUAL_INCOME INT,
DTI FLOAT,
INSTALLMENT FLOAT,
INT_RATE FLOAT,
LOAN_AMOUNT INT,
TOTAL_ACC INT,
TOTAL_PAYMENT INT);

LOAD DATA INFILE
'D:\financial_loan1.csv'
INTO TABLE FINANCIAL_LOAN
